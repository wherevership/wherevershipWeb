    <p>The list below is the terms and conditions of carriage:</p>
    <ol>
      <li>
        <p>Personal data means information in respect of commercial transactions that relates to directly or indirectly to a Data Subject, who is identified or identifiable 
		from that information or from that and other information in the possession of a data user, including any sensitive personal data, and expression of opinion 
		about the Data Subject. Such personal information about or which identifies individual(s) who is the Data Subject, includes but not limited to individual(s)' 
		names, addresses, identity card numbers (MyKad), passport numbers, telephone and/or facsimile numbers, photographs, finger prints, and email and/or social 
		media addresses.</p>
      </li>
      <li>
        <p>Processing in relation to personal data means collecting, recording, holding or storing the personal data or carrying out any operations or set of operations 
		on the personal data.</p>
      </li>
      <li>
        <p>Sensitive personal data means any personal information consisting of information including but not limited to the physical or mental health or condition of a 
		Data Subject, his political opinions, his religious beliefs or other beliefs of a similar nature, the commission or alleged commission by him of any offence
		 or any other personal data.</p>
      </li>
      <li>
        <p>Data Subject refers to individual who is the subject of the personal data.</p>
      </li>
    </ol>
