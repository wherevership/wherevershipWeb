      $("html, body").animate({ scrollTop: $("[name=txt_repass]").offset().top - 70 }, 1000);
    }
  }
  if(datas.email == ""){
    pass = false;
    doFail($("[name=txt_email]"));
    message += "Please Fill In Your Register Email.<br>";
    variable = "[name=txt_email]";
  }else if(!General.validateEmail(datas.email)){
    pass = false;
